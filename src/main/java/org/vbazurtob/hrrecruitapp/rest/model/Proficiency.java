package org.vbazurtob.hrrecruitapp.rest.model;

public class Proficiency {

	private long score;
	private String text;
	
	public Proficiency() {
	}

	public long getScore() {
		return score;
	}

	public void setScore(long score) {
		this.score = score;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}
	
	

}
